export default function changeTaskInputDisplay() {
    let taskInput = document.getElementById('task-container');

    if (taskInput.style.display === 'none') {
    taskInput.style.display === 'block'
    } else {
        taskInput.style.display === 'none'
    };
}

export default  function changeProjectInputDisplay() {
    let projectInput = document.getElementById('project-container');
    if (projectInput.style.display === 'none') {
        projectInput.style.display === 'block'
    } else {
        projectInput.style.display === 'none'
    };
}